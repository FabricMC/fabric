package net.fabricmc.fabric.mixin.client.texture;

import net.minecraft.client.texture.NativeImage;
import net.minecraft.client.texture.Sprite;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.gen.Accessor;

/**
 * Simply provides access to {@linkplain Sprite#images}.
 * Do shy away from casting to this. If you feel the need to, {@see ColorSniffer#getImagesForSprite}.
 */
@Mixin(Sprite.class)
public interface SpriteAccessor {
	@Accessor("images")
	NativeImage[] getImages();
}
